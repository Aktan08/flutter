class AppColors {
  static Color appbarbackground = Colors.black;
  static Color background = Colors.white;

  getTextColor() {
    if (background == Colors.white) {
      return Colors.black;
    } else {
      Colors.white;
    }
  }

  static Color textdark = Colors.black;
  static Color textDark = Colors.white38;
}
